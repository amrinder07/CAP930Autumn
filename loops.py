#Loops
#Odd loop: when no of conditions is not known is called odd loop.
#Range: used to iterate over a sequence of numbers.

for i in range(1,7):    #last index is exclusive
    print(i)

for i in range(2,12,3):    #last index is exclusive
    print(i)

for i in range(-7,-30,-5):    #last index is exclusive
    print(i)
